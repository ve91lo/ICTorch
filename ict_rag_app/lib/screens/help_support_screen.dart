              question: 'Can I use the app offline?',
              answer: 'Notes and quiz results are cached locally for offline review. However, the AI chat and generating new content requires an internet connection.',
            ),
            _FaqTile(
              question: 'How are quizzes generated?',
              answer: 'Quizzes are dynamically generated by AI based on the topic content. Each attempt may have different questions to ensure comprehensive learning.',
