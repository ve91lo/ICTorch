        padding: const EdgeInsets.all(20),
        child: Column(
          crossAxisAlignment: CrossAxisAlignment.start,
          children: [
            // FAQ Section
            _buildSectionTitle('Frequently Asked Questions'),
            const SizedBox(height: 12),
            _FaqTile(
              question: 'How does the AI tutor work?',
              answer: 'The AI tutor uses RAG (Retrieval-Augmented Generation) technology to provide accurate answers based on the HKDSE ICT curriculum. It searches through course materials and generates personalised responses.',
            ),
